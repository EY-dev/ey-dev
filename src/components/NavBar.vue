<script>
import HamburgerBtn from "@/components/HamburgerBtn";
export default {
    name: "NavBar",
    components: {
        HamburgerBtn
    },
    data: () => ({
        menu:null,
        rect_menu: null
    }),
    methods:{
        openMenu(){
            this.menu.classList.toggle('active');
        },
        handleScroll(){
            if (document.body.scrollTop <= window.innerHeight - 150 || document.documentElement.scrollTop <=  window.innerHeight - 150) {
                this.rect_menu.classList.remove('navigations-light');
                this.rect_menu.classList.remove('navigations-dark');
            }
            if (document.documentElement.scrollTop >=  window.innerHeight - 150) {
                this.rect_menu.classList.remove('navigations-light');
                this.rect_menu.classList.add('navigations-dark');
            }
            if (document.documentElement.scrollTop >=  3*window.innerHeight - 150) {
                this.rect_menu.classList.remove('navigations-dark');
                this.rect_menu.classList.add('navigations-light');
            }

            if (document.documentElement.scrollTop > window.innerHeight/2) {
                document.querySelector('.top').classList.add('show')
            }
            else{
                document.querySelector('.top').classList.remove('show')
            }
            if (document.documentElement.scrollTop > window.innerHeight-50) {
                document.querySelector('.bottom').classList.add('bottom-show')
            }
            else{
                document.querySelector('.bottom').classList.remove('bottom-show')
            }
            if (document.documentElement.scrollTop > window.innerHeight+150) {
                document.querySelector('.advantages h2').classList.add('active');
                document.querySelector('.phone-pic').classList.add('active')
            }
            else{
                document.querySelector('.advantages h2').classList.remove('active')
                document.querySelector('.phone-pic').classList.remove('active')
            }
            if (document.documentElement.scrollTop > 2*window.innerHeight-150) {
                document.querySelector('.advantages ul').classList.add('active');
            }
            else{
                document.querySelector('.advantages ul').classList.remove('active')
            }

        }
    },
    mounted() {
        this.rect_menu = document.querySelector('.navigations');
        this.menu = document.querySelector('.navigations ul');
        const links = document.querySelectorAll('.navigations ul li');
        const menuBtn = document.querySelector('.hamburger-button');
        links.forEach(link => {
            link.addEventListener('click', ()=>{
                this.menu.classList.remove('active');
                menuBtn.classList.remove('active');
            })
        })

    },
    created () {
        window.addEventListener('scroll', this.handleScroll);
    },
    destroyed () {
        window.removeEventListener('scroll', this.handleScroll);
    }
}
</script>

